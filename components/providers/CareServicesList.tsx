// ── Service → Icon mapping (case-insensitive keyword match) ──

type IconComponent = ({ className }: { className?: string }) => React.JSX.Element;

const SERVICE_ICON_MAP: [RegExp, IconComponent][] = [
  // Nursing & skilled care
  [/(skilled nursing|nursing care|24.hour|wound care|iv therapy)/i, IconNursing],
  // Therapy
  [/(physical therapy|occupational therapy|speech therapy|rehab)/i, IconTherapy],
  // Bathing & personal hygiene
  [/(bath|groom|shower|hygiene|dressing|toileting|incontinence)/i, IconBathing],
  // Meals & nutrition
  [/(meal|dining|nutrition|food|snack|cook)/i, IconMeal],
  // Medication
  [/(medication|med manage|med remind|pharmacy)/i, IconMedication],
  // Home & housekeeping
  [/(housekeep|laundry|maintenance|cleaning|home care|in.home)/i, IconHome],
  // Transportation
  [/(transport|errand|shopping|escort|driving)/i, IconTransport],
  // Social & community
  [/(social|communit|companion|recreation|program|event|concierge)/i, IconSocial],
  // Exercise & wellness
  [/(exercise|fitness|wellness|strength|balance|fall prev|yoga)/i, IconExercise],
  // Cognitive & memory
  [/(cognit|memory care|brain|stimulat|structured daily)/i, IconCognitive],
  // Emotional & spiritual
  [/(emotion|spiritual|bereave|comfort care|counsel|family support|family educ)/i, IconEmotional],
  // Safety & security
  [/(emergency|secure|safety|background|drug screen|reference check|certified)/i, IconSafety],
  // Education
  [/(education|patient edu|training|work eligib)/i, IconEducation],
  // Personal care & mobility
  [/(personal care|mobility|walking|wheelchair|assist)/i, IconMobility],
  // Pain & symptom management
  [/(pain|symptom|palliative)/i, IconPain],
  // Overnight & respite
  [/(overnight|respite|24.hr|round.the.clock)/i, IconClock],
  // Health monitoring
  [/(health monitor|health screen|vitals|wellness check)/i, IconRehab],
  // Behavioral
  [/(behavior)/i, IconCognitive],
  // Post-surgical
  [/(post.surg|recovery)/i, IconRehab],
];

function getServiceIcon(service: string): IconComponent {
  for (const [pattern, Icon] of SERVICE_ICON_MAP) {
    if (pattern.test(service)) return Icon;
  }
  return IconDefault;
}

// ── Component ──

export default function CareServicesList({
  services,
  initialCount = 15,
}: CareServicesListProps) {
  const [expanded, setExpanded] = useState(false);
  const needsExpand = services.length > initialCount;
  const visible =
    needsExpand && !expanded ? services.slice(0, initialCount) : services;

  return (
    <div>
      <div className="grid grid-cols-1 sm:grid-cols-2 gap-x-12 gap-y-4">
        {visible.map((service) => {
          const Icon = getServiceIcon(service);
          return (
            <div key={service} className="flex items-center gap-4 py-1.5">
              <Icon className="w-6 h-6 text-gray-600 flex-shrink-0" />
              <span className="text-[15px] text-gray-700">{service}</span>
            </div>
          );
        })}
      </div>
      {needsExpand && (
        <button
          onClick={() => setExpanded(!expanded)}
          className="mt-6 px-5 py-2.5 text-sm font-medium text-gray-800 bg-white border border-gray-300 rounded-lg hover:bg-gray-50 transition-colors"
        >
          {expanded ? "Show less" : `Show all ${services.length} services`}
        </button>
      )}
    </div>
  );
}
