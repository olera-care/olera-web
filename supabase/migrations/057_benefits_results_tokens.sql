-- Migration: benefits_results_tokens table for /m/{token} addressable results
--
-- Context: V3 of the embedded SBF (2-step redesign) replaces the post-submit
-- redirect to /welcome with a side-panel/bottom-sheet overlay on the provider
-- page. The same matches surface is also addressable at /m/{token} as a
-- standalone page reachable from the welcome email/SMS magic link.
--
-- The token is the auth — anyone with the URL sees the matches. No login
-- wall on /m/{token}. Tokens are short (16-char base64url), unguessable,
-- one per family profile (one row per intake completion).
--
-- Schema:
--   token            — base64url-safe, generated by lib/benefits-token.ts
--   profile_id       — FK to business_profiles (the family profile, type='family')
--   care_need        — what the user picked at step 1 (replays the match logic)
--   state_code       — 2-letter, e.g. 'TX' (replays the match logic)
--   provider_slug    — provider page they came from, for the "Some of these may
--                       help cover services at {Provider X}" tie-in copy. Null
--                       if visited via /m/{token} from outside a provider page.
--   match_count      — the count we showed at submit time (snapshot for
--                       analytics + comparison if program data drifts later)
--   created_at       — when the token was issued
--   last_viewed_at   — bumped each time /m/{token} or the in-session overlay
--                       opens. Used for engagement analytics (how many
--                       captured users come back via the link).
--
-- Apply via Supabase dashboard (NOT CLI).

CREATE TABLE IF NOT EXISTS benefits_results_tokens (
  token TEXT PRIMARY KEY,
  profile_id UUID NOT NULL REFERENCES business_profiles(id) ON DELETE CASCADE,
  care_need TEXT NOT NULL CHECK (care_need IN (
    'stayingAtHome',
    'payingForCare',
    'memoryHealth',
    'companionship'
  )),
  state_code TEXT NOT NULL,
  provider_slug TEXT,
  match_count INT NOT NULL DEFAULT 0,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  last_viewed_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_benefits_results_tokens_profile_id
  ON benefits_results_tokens(profile_id);
